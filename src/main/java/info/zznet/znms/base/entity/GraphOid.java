package info.zznet.znms.base.entity;

/**
 * 图形和oid对应关系实体类
 * @author dell001
 *
 */
public class GraphOid {
    private String graphOidUuid;
    
    //图形uuid
    private String graphUuid;

    private String hostUuid;

    private String hostIp;

    //图形模板ID
    private String graphTemplateId;

    //接口图形时OID为oid+index
    private String oid;

    //流量方向  流入：in;流出:out
    private String flowDirection;

    //rrd文件名
    private String rrdFileName;

    public String getGraphOidUuid() {
        return graphOidUuid;
    }

    public void setGraphOidUuid(String graphOidUuid) {
        this.graphOidUuid = graphOidUuid;
    }
    
    public String getGraphUuid() {
		return graphUuid;
	}

	public void setGraphUuid(String graphUuid) {
		this.graphUuid = graphUuid;
	}

	public String getHostUuid() {
        return hostUuid;
    }

    public void setHostUuid(String hostUuid) {
        this.hostUuid = hostUuid;
    }

    public String getHostIp() {
        return hostIp;
    }

    public void setHostIp(String hostIp) {
        this.hostIp = hostIp;
    }

    public String getGraphTemplateId() {
        return graphTemplateId;
    }

    public void setGraphTemplateId(String graphTemplateId) {
        this.graphTemplateId = graphTemplateId;
    }

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getFlowDirection() {
        return flowDirection;
    }

    public void setFlowDirection(String flowDirection) {
        this.flowDirection = flowDirection;
    }

    public String getRrdFileName() {
        return rrdFileName;
    }

    public void setRrdFileName(String rrdFileName) {
        this.rrdFileName = rrdFileName;
    }
}