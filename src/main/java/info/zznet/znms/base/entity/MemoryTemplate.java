package info.zznet.znms.base.entity;

import java.util.Date;
/**
 * 内存统计模板实体
 * @author dell001
 *
 */
public class MemoryTemplate {
    private String memoryTemplateUuid;

    private String graphTemplateUuid;

    private Date createTime;
    
    private GraphTemplate graphTemplate;

    public String getMemoryTemplateUuid() {
        return memoryTemplateUuid;
    }

    public void setMemoryTemplateUuid(String memoryTemplateUuid) {
        this.memoryTemplateUuid = memoryTemplateUuid;
    }

    public String getGraphTemplateUuid() {
		return graphTemplateUuid;
	}

	public void setGraphTemplateUuid(String graphTemplateUuid) {
		this.graphTemplateUuid = graphTemplateUuid;
	}

	public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

	public GraphTemplate getGraphTemplate() {
		return graphTemplate;
	}

	public void setGraphTemplate(GraphTemplate graphTemplate) {
		this.graphTemplate = graphTemplate;
	}
    
}