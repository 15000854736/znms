package info.zznet.znms.base.entity;

import java.util.Date;

public class FlowData {
    private String id;

    private Double teacherWirelessFlow;

    private Double teacherPcFlow;

    private Double studentWirelessFlow;

    private Double studentPcFlow;

    private Date recordDate;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Double getTeacherWirelessFlow() {
        return teacherWirelessFlow;
    }

    public void setTeacherWirelessFlow(Double teacherWirelessFlow) {
        this.teacherWirelessFlow = teacherWirelessFlow;
    }

    public Double getTeacherPcFlow() {
        return teacherPcFlow;
    }

    public void setTeacherPcFlow(Double teacherPcFlow) {
        this.teacherPcFlow = teacherPcFlow;
    }

    public Double getStudentWirelessFlow() {
        return studentWirelessFlow;
    }

    public void setStudentWirelessFlow(Double studentWirelessFlow) {
        this.studentWirelessFlow = studentWirelessFlow;
    }

    public Double getStudentPcFlow() {
        return studentPcFlow;
    }

    public void setStudentPcFlow(Double studentPcFlow) {
        this.studentPcFlow = studentPcFlow;
    }

    public Date getRecordDate() {
        return recordDate;
    }

    public void setRecordDate(Date recordDate) {
        this.recordDate = recordDate;
    }
}