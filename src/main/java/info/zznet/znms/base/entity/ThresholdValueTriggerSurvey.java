package info.zznet.znms.base.entity;

/**
 * 阀值告警 --阀值触发概况实体
 * @author dell001
 *
 */
public class ThresholdValueTriggerSurvey {
    private String surveyUuid;

    //阀值uuid
    private String thresholdValueUuid;

    //最近一小时触发次数
    private Integer lastHourTriggerCount;

    //最近一天触发次数
    private Integer lastDayTriggerCount;

    //最近一周触发次数
    private Integer lastWeekTriggerCount;

    public String getSurveyUuid() {
        return surveyUuid;
    }

    public void setSurveyUuid(String surveyUuid) {
        this.surveyUuid = surveyUuid;
    }

    public String getThresholdValueUuid() {
        return thresholdValueUuid;
    }

    public void setThresholdValueUuid(String thresholdValueUuid) {
        this.thresholdValueUuid = thresholdValueUuid;
    }

    public Integer getLastHourTriggerCount() {
        return lastHourTriggerCount;
    }

    public void setLastHourTriggerCount(Integer lastHourTriggerCount) {
        this.lastHourTriggerCount = lastHourTriggerCount;
    }

    public Integer getLastDayTriggerCount() {
        return lastDayTriggerCount;
    }

    public void setLastDayTriggerCount(Integer lastDayTriggerCount) {
        this.lastDayTriggerCount = lastDayTriggerCount;
    }

    public Integer getLastWeekTriggerCount() {
        return lastWeekTriggerCount;
    }

    public void setLastWeekTriggerCount(Integer lastWeekTriggerCount) {
        this.lastWeekTriggerCount = lastWeekTriggerCount;
    }
}