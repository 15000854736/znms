package info.zznet.znms.base.entity;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 阀值实体类
 * @author dell001
 *
 */
public class ThresholdValue {
    private String thresholdValueUuid;

    //主机uuid
    private String hostUuid;

    //图形uuid
    private String graphUuid;

    //流向  0:无流向(基本图形);1:流入(接口图形);2:流出(接口图形)
    private Integer flowDirection;

    //阀值名称
    private String thresholdValueName;

    //阀值启用状态
    private Integer status;

    //警告高阀值
    private BigDecimal warningHighThresholdValue;

    //警告低阀值
    private BigDecimal warningLowThresholdValue;

    //创建时间
    private Date createTime;
    
    private Host host;
    
    private Graph graph;

    public String getThresholdValueUuid() {
        return thresholdValueUuid;
    }

    public void setThresholdValueUuid(String thresholdValueUuid) {
        this.thresholdValueUuid = thresholdValueUuid;
    }

    public String getHostUuid() {
        return hostUuid;
    }

    public void setHostUuid(String hostUuid) {
        this.hostUuid = hostUuid;
    }

    public String getGraphUuid() {
        return graphUuid;
    }

    public void setGraphUuid(String graphUuid) {
        this.graphUuid = graphUuid;
    }

    public Integer getFlowDirection() {
        return flowDirection;
    }

    public void setFlowDirection(Integer flowDirection) {
        this.flowDirection = flowDirection;
    }

    public String getThresholdValueName() {
        return thresholdValueName;
    }

    public void setThresholdValueName(String thresholdValueName) {
        this.thresholdValueName = thresholdValueName;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public BigDecimal getWarningHighThresholdValue() {
		return warningHighThresholdValue;
	}

	public void setWarningHighThresholdValue(BigDecimal warningHighThresholdValue) {
		this.warningHighThresholdValue = warningHighThresholdValue;
	}

	public BigDecimal getWarningLowThresholdValue() {
		return warningLowThresholdValue;
	}

	public void setWarningLowThresholdValue(BigDecimal warningLowThresholdValue) {
		this.warningLowThresholdValue = warningLowThresholdValue;
	}

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

	public Host getHost() {
		return host;
	}

	public void setHost(Host host) {
		this.host = host;
	}

	public Graph getGraph() {
		return graph;
	}

	public void setGraph(Graph graph) {
		this.graph = graph;
	}
    
}