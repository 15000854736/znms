package info.zznet.znms.base.entity;

/**
 * AP范围
 * @author dell001
 *
 */
public class ApRegion {
    private String apRegionUuid;

    //区域名
    private String apRegionName;

    //区域坐标
    private String apRegionCoordinate;

    public String getApRegionUuid() {
        return apRegionUuid;
    }

    public void setApRegionUuid(String apRegionUuid) {
        this.apRegionUuid = apRegionUuid;
    }

    public String getApRegionName() {
        return apRegionName;
    }

    public void setApRegionName(String apRegionName) {
        this.apRegionName = apRegionName;
    }

    public String getApRegionCoordinate() {
        return apRegionCoordinate;
    }

    public void setApRegionCoordinate(String apRegionCoordinate) {
        this.apRegionCoordinate = apRegionCoordinate;
    }
}