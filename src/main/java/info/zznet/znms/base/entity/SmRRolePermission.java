package info.zznet.znms.base.entity;

public class SmRRolePermission {
    private String rRolePermissionUuid;

    private String roleUuid;

    private String permissionUuid;

    public SmRRolePermission(String rRolePermissionUuid, String roleUuid, String permissionUuid){
    	this.rRolePermissionUuid = rRolePermissionUuid;
    	this.roleUuid = roleUuid;
    	this.permissionUuid = permissionUuid;
    }

	public String getrRolePermissionUuid() {
        return rRolePermissionUuid;
    }

    public void setrRolePermissionUuid(String rRolePermissionUuid) {
        this.rRolePermissionUuid = rRolePermissionUuid;
    }

    public String getRoleUuid() {
        return roleUuid;
    }

    public void setRoleUuid(String roleUuid) {
        this.roleUuid = roleUuid;
    }

    public String getPermissionUuid() {
        return permissionUuid;
    }

    public void setPermissionUuid(String permissionUuid) {
        this.permissionUuid = permissionUuid;
    }
}