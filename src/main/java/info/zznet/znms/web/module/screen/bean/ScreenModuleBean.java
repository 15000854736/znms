package info.zznet.znms.web.module.screen.bean;

public class ScreenModuleBean{
	
	public ScreenModuleBean(String moduleName, int width,int height,String spec,String moduleChineseName) {
		this.moduleName = moduleName;
		this.width = width;
		this.height = height;
		this.spec = spec;
		this.moduleChineseName = moduleChineseName;
	}
	public ScreenModuleBean(){
		
	}
	
	private String moduleName;
	private int width;
	private int height;
	private String spec;
	private String moduleChineseName;
	
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public String getSpec() {
		return spec;
	}
	public void setSpec(String spec) {
		this.spec = spec;
	}
	public String getModuleChineseName() {
		return moduleChineseName;
	}
	public void setModuleChineseName(String moduleChineseName) {
		this.moduleChineseName = moduleChineseName;
	}
	
}