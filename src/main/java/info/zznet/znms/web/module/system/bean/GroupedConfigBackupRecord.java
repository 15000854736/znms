package info.zznet.znms.web.module.system.bean;

import java.util.Date;

public class GroupedConfigBackupRecord {
	String hostUuid;
	String hostName;
	int recordCount;
	String hostIp;
	String backupPath;
	Date latestBackupTime;
	String latestActivator;
	Boolean useStatus;
	
	public String getHostUuid() {
		return hostUuid;
	}
	public void setHostUuid(String hostUuid) {
		this.hostUuid = hostUuid;
	}
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public int getRecordCount() {
		return recordCount;
	}
	public void setRecordCount(int recordCount) {
		this.recordCount = recordCount;
	}
	public String getHostIp() {
		return hostIp;
	}
	public void setHostIp(String hostIp) {
		this.hostIp = hostIp;
	}
	public String getBackupPath() {
		return backupPath;
	}
	public void setBackupPath(String backupPath) {
		this.backupPath = backupPath;
	}
	public Date getLatestBackupTime() {
		return latestBackupTime;
	}
	public void setLatestBackupTime(Date latestBackupTime) {
		this.latestBackupTime = latestBackupTime;
	}
	public String getLatestActivator() {
		return latestActivator;
	}
	public void setLatestActivator(String latestActivator) {
		this.latestActivator = latestActivator;
	}
	public Boolean getUseStatus() {
		return useStatus;
	}
	public void setUseStatus(Boolean useStatus) {
		this.useStatus = useStatus;
	}
	
}
